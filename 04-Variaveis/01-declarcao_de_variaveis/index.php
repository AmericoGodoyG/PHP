<?php
    /*   * * * VARIÁVEL * * * 
        Declarar valores e salvá-lo na memoria
        $ determina que é uma variável
        $nome="Matehus";
        Podemos salves qualquer tipo de dados
        Podemos alterar o valor de uma variavel
        Exibir um valor de uma variável echo
    */

    $teste = "Texto";
    echo $teste;

    echo "<br>";

    $num = 10;
    echo $num;

    echo "<br>";

    $_nome = "Américo";
