<?php 
    /*
        * * * O que são classes * * *
        
        Uma classe é um estrurura fundamental
        que serve como um modelo ou molde para
        criar objetos.

        Ela define um conjunto de proprieddes
        (também chamdas de atributos) e métodos
        (ou comportmentos) que os objetos criados
        a partir dela terão.

        Em outras palavras, uma classe descreve a 
        estrutrura e o comportamento de seus objetos.

        Em PHP sempre que vamos criar ou instanciar
        um objeto vamos precisar de uma classe, porque
        um objeto é instanciado a partir da classe

        Podemos mudar o valor para cada objeto criado,
        mas ele parte do que a classe impõe.
    */
?>