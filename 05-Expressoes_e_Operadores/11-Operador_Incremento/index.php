<?php 
    /*
        * * * Auto incremento e decremento * * *

        Incrementar: ++;
        Decrementar: --;

        Ex: $n++ ou $x--;
        n e x serão alterados com +1 ou -1;
        
        Utilizados em estruturas de repetição.
    */

    $x = 0;
    $y = 0;

    //Incremento
    $x++;
    $y = $y + 1;

    echo "x: " . $x . "<br>";
    echo "y: " . $y . "<br>";
    
    $m = 10;
    $n = 10;

    //Decremento
    $n--;
    $m = $m - 1;

    echo "m: " . $m . "<br>";
    echo "n: " . $n . "<br>";

?>