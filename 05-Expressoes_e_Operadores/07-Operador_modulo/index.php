<?php 
    /*
        * * * Operador de módulo (%) * * * 
        O operador de módulo é inserido no código pelo simbolo %
        Sua função é realizar uma divisão.
        Mas como resultado ele apresenta apenas o resto da mesma.
    */

    //Módulo
    echo "Resto = " . 12 % 2;
    echo "<br>";

    //Divisão
    echo "Quociente = ". 12 / 2;
    echo "<br>";

    $a = 20;
    $b = 4;
    $c = 12; 

    echo $a % $b;
    echo "<br>";
    echo $a % $c;

?>